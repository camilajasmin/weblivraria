package br.com.weblivraria.dominio;

public class USER {
	private Integer IDUser;
	private String NOMEuser;
	private String SENHAuser;
	private String EMAILuser;
	private String TELEFONEuser;
	private String NOMECOMPLETOuser;
	private String CPFuser;
	
	
	public Integer getIDUser() {
		return IDUser;
	}
	public void setIDuser(Integer idUser) {
		this.IDUser = idUser;
	}
	public String getNomeuser() {
		return NOMEuser;
	}
	public void setNomeuser(String nomeuser) {
		NOMEuser = nomeuser;
	}
	public String getSENHAuser() {
		return SENHAuser;
	}
	public void setSENHAuser(String sENHAuser) {
		SENHAuser = sENHAuser;
	}
	public String getEMAILuser() {
		return EMAILuser;
	}
	public void setEMAILuser(String eMAILuser) {
		EMAILuser = eMAILuser;
	}
	public String getTELEFONEuser() {
		return TELEFONEuser;
	}
	public void setTELEFONEuser(String tELEFONEuser) {
		TELEFONEuser = tELEFONEuser;
	}
	public String getNOMECOMPLETOuser() {
		return NOMECOMPLETOuser;
	}
	public void setNOMECOMPLETOuser(String nOMECOMPLETOuser) {
		NOMECOMPLETOuser = nOMECOMPLETOuser;
	}
	public String getCPFuser() {
		return CPFuser;
	}
	public void setCPFuser(String cPFuser) {
		CPFuser = cPFuser;
	}

}
